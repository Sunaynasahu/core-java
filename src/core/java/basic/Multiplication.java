package core.java.basic;

public class Multiplication {
	public static void main(String[]args){
		int a= 20;
		int b= 40;
		int c= 30;
		
		int d= a+b*c;
		
		a= a+b;  // a= 60
		
		b= a-b;  // b= 20
		
		c= a*b*c;  // c= 36000
		
		System.out.println(a);
		
		System.out.println(c);
		
		System.out.println(d);
		
		
		
		
		
		
	}

}
