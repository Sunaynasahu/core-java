package Armstrong.java;

public class Array1 {
	public static void main(String[] args) {
		
		int[] tables = {3, 6, 9, 12, 15, 18, 21, 24, 27, 30};
		
		String[] names = {"abc","edf","xyz"};
		
		for(int i = 0; i< tables.length; i++) {
			
			System.out.print(tables[i]);
			System.out.print(" ");
		}
		
		
		for(String s : names) {
			
	     System.out.println(s);
	     
	     System .out.println(" ") ;
		}
	  
	}

}
